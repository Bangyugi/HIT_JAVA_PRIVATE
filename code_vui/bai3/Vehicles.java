package code_vui.bai3;

public class Vehicles {
    private String maker;
    private String model;
    private int price;

    public Vehicles() {
    }

    public Vehicles(String maker, String model, int price) {
        this.maker = maker;
        this.model = model;
        this.price = price;
    }

    public String getMaker() {
        return maker;
    }

    public void setMaker(String maker) {
        this.maker = maker;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public void input()
    {

    }

    public void output()
    {}
}
